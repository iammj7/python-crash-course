# LIST

bicycle = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycle)

#  ACCESSING ELEMENTS IN A LIST
print(bicycle[0])
#  STRING METHOD TO FORMART AN ELEMENT IN LIST
print(bicycle[0].title())

#  INDEX STARTS AT 0 NOT 1
print(bicycle[1])
print(bicycle[0])

# ACCESSING THE LAST ELEMENT IN A LIST
print(bicycle[-1])

#  USE INDIVIDUAL VALUES FROM A LIST
message = f"My first bicycle was a {bicycle[0].title()}"
print(message)
